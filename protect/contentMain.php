<h1>生命的本质就是创新</h1>

有人说，一个人的“最大的敌人是你自己”，我想问的是：我们为什么会把自己当敌人？又有人说，“生命的出现是一个奇迹”，而我想说的是，会把自己当成自己的敌人的生命确实是奇迹中的奇迹。天地之间，茫茫宇宙，时刻进行着无尽的信息产生和消失，而消失的信息元素往往又会重新凝聚起来成为新的信息之源。宇宙在继续向外探索，生命通过遗传而不断的进化，大脑则在不断的思索，并在思索中得以不断的优化。这就是生命的特征，通过创新来证明自己的存在，而创新的不断演进，也是催生更高级的生命的必要途径。换言之，生命的本质就是创新，或者说，创新是确保生命万世永存的秘密。因此，在下一个5年，10年，。。。。1万年中，让所有人与我们一起，继续、不断地创新吧！
